package day02;

public class 실습하기4 {

	public static void main(String[] args) {

		System.out.println('\"'); // 보통 특수문자를 읽지 못할때 \를 사용하자!
		// \t탭의 크기만큼 띄워 쓰기를 한다.(Tab), \b출력을 할때 마지막문자를 지운다.(Backspace)
		System.out.println("abc\t123\b456");
		System.out.println('\n'); // 줄바꿈
		System.out.println("\"Hello\""); // 문자열안에서 ""을 사용하고 싶으면 \를 이용하자!
		System.out.println("c:\\"); // 문자열안에서 \을 사용하고 싶으면 \를 이용하자!

	}

}